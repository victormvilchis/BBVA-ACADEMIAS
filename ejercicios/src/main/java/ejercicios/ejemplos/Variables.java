package ejercicios.ejemplos;

public class Variables {

	public static void main(String[] args) {
		// CREAR VARIABLES
		int años = 20;
		// MODIFICAR VARIABLES
		años += 5;
		// IMPRIMIR NUEVO VALOR
		System.out.println("El nuevo valor de la variable es: " + años);
		// VARIABLES WRAPPER
		Integer hijos = 2;
		String nombre = "Efrain";
		String apellidos = "Cruz Lobato";
		// IMPRIMIMOS MENSAJE Y RESULTADO
		System.out.println("Hola mi nombre es " + nombre + " " + apellidos + ", tengo " + años + " años " + "y tengo "
				+ hijos + " hijos.");

	}

}
