package ejercicios.ejemplos;

public class Constantes {

	public static void main(String[] args) {
		// CREA CONSTANTE
		final double PI = 3.14159;
		// CREAR VARIABLE
		int RADIO = 10;
		// REALIZAR OPERACION
		double area = PI * RADIO * RADIO;
		// IMPRIMIMOS MENSAJE Y RESULTADO
		System.out.println("El area es: " + area);
	}
}
