//Segundo programa que calcule el area de un circulo 

package academia.java;

public class segundoPrograma {
	public static void main(String[] args) {
		
		double pi = 3.1416;
		double radio = 10;
		double area = pi * radio * radio;
		
		System.out.println("Hola, el area de un circulo con radio "
				+ radio + " es: " + area);
	}

}
